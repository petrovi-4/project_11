def upper_string(string):
	"""
	Фенкция, которая из любой строки делает строку в вухнем регистре.
	:param string: строка
	:return: строка в верзнем регистре
	"""
	return string.upper()


def title_string(string):
	"""
	Функция, которая делает каждое слово в строке с заглавной буквы.
	:param string: строка
	:return: строка со словами с заглавной буквы
	"""
	return string.title()
